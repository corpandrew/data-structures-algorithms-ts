// eslint-disable-next-line @typescript-eslint/ban-ts-comment
// @ts-expect-error
// eslint-disable-next-line @typescript-eslint/no-unused-vars
function generateMaze(length: number): number[][] {
    const maze: number[][] = [];

    for (let row = 0; row < length; row++) {
        const row: number[] = [];
        for (let col = 0; col < length; col++) {
            row[col] = Math.round(Math.random());
        }
        maze.push(row);
    }

    //Make the start and end (length-1, length-1) open
    if (length > 0) {
        maze[0][0] = 0;
        if (length > 1) {
            maze[length - 1][length - 1] = 0;
        }
    }

    return maze;
}